    private void mostrarFactura() {

        txtDetalleFactura.setText("");

        if (ordenActual == null) {

            txtDetalleFactura.setText(
                    "No hay una orden para facturar.");

            return;
        }

        // El total de la orden ya incluye el IVA
        double total = ordenActual.calcularTotal();

        // Se obtiene el subtotal sin IVA
        double subtotal = total / 1.13;

        // Se obtiene cuánto corresponde al IVA
        double impuesto = total - subtotal;

        txtDetalleFactura.append(
                "FIDEBURGUESAS\n");

        txtDetalleFactura.append(
                "====================================\n");

        txtDetalleFactura.append(
                "Orden #: "
                + ordenActual.getId()
                + "\n");

        txtDetalleFactura.append(
                "Cliente: "
                + ordenActual.getCliente()
                + "\n");

        if (cajeroActual != null) {

            txtDetalleFactura.append(
                    "Cajero: "
                    + cajeroActual.getNombre()
                    + "\n");
        }

        txtDetalleFactura.append(
                "Fecha: "
                + ordenActual.getFecha()
                + "\n");

        txtDetalleFactura.append(
                "Estado: "
                + ordenActual.getEstado()
                + "\n");

        txtDetalleFactura.append(
                "====================================\n");

        txtDetalleFactura.append(
                "Detalle:\n\n");

        for (ItemOrden item : ordenActual.getItems()) {

            if (item.esProducto()) {

                txtDetalleFactura.append(
                        item.getCantidad()
                        + " x "
                        + item.getProducto().getNombre()
                        + " - ₡"
                        + String.format("%.2f", item.getSubtotal())
                        + "\n");

            } else if (item.esCombo()) {

                txtDetalleFactura.append(
                        item.getCantidad()
                        + " x Combo: "
                        + item.getCombo().getNombre()
                        + " - ₡"
                        + String.format("%.2f", item.getSubtotal())
                        + "\n");
            }
        }

        txtDetalleFactura.append(
                "\n------------------------------------\n");

        txtDetalleFactura.append(
                "Subtotal (sin IVA): ₡"
                + String.format("%.2f", subtotal)
                + "\n");

        txtDetalleFactura.append(
                "IVA (13% incluido): ₡"
                + String.format("%.2f", impuesto)
                + "\n");

        txtDetalleFactura.append(
                "TOTAL: ₡"
                + String.format("%.2f", total)
                + "\n");

        txtDetalleFactura.append(
                "====================================\n");
    }
    
    private void actualizarOrdenGuardada() {

        ArchivosOrdenes archivoOrdenes =
                new ArchivosOrdenes();

        ArrayList<Orden> ordenes =
                archivoOrdenes.leerOrdenes();

        boolean encontrada = false;

        for (int i = 0; i < ordenes.size(); i++) {

            if (ordenes.get(i).getId()
                    == ordenActual.getId()) {

                ordenes.set(i, ordenActual);

                encontrada = true;

                break;
            }
        }

        if (!encontrada) {

            ordenes.add(ordenActual);
        }

        ArchivosOrdenes.guardarOrdenes(ordenes);
    }
    

