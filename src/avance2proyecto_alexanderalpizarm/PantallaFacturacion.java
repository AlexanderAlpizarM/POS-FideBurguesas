    private void btnMarcarPagadaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMarcarPagadaActionPerformed
        if (ordenActual == null) {

            JOptionPane.showMessageDialog(null,
                    "No hay una orden para marcar como pagada.");

            return;
        }

        if (ordenPagada) {

            JOptionPane.showMessageDialog(null,
                    "La orden ya fue marcada como pagada.");

            return;
        }

        ArchivosFacturas archivoFacturas =
                new ArchivosFacturas();

        ArrayList<Factura> facturas =
                archivoFacturas.leerFacturas();

        int idFactura = 1;

        for (Factura factura : facturas) {

            if (factura.getId() >= idFactura) {

                idFactura = factura.getId() + 1;
            }
        }

        facturaActual = new Factura(idFactura, ordenActual);

        facturaActual.generarFactura();

        facturas.add(facturaActual);

        ArchivosFacturas.guardarFacturas(facturas);

        ordenActual.cambiarEstado("Pagada");

        actualizarOrdenGuardada();

        ordenPagada = true;

        btnMarcarPagada.setEnabled(false);

        mostrarFactura();

        JOptionPane.showMessageDialog(null,
                "Orden marcada como pagada.");
    }//GEN-LAST:event_btnMarcarPagadaActionPerformed

