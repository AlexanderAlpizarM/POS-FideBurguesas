    private void BCancelarBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BCancelarBorrarActionPerformed
        if (ordenActual == null) {
            JOptionPane.showMessageDialog(null,"No hay una orden para cancelar.");
            return;
        }

        int opcion = JOptionPane.showConfirmDialog(null,"¿Desea cancelar la orden actual?","Cancelar Orden",JOptionPane.YES_NO_OPTION);

        if (opcion == JOptionPane.YES_OPTION){
            ordenActual = null;
            txtOrdenActual.setText("No hay una orden creada.");

            JOptionPane.showMessageDialog(null,"Orden cancelada correctamente.");
        }
    }//GEN-LAST:event_BCancelarBorrarActionPerformed

    private void BEnviarCocinaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BEnviarCocinaActionPerformed
        if (ordenActual == null){
            JOptionPane.showMessageDialog(null,"No hay una orden para enviar a cocina.");
            return;
        }

        // Verificar que la orden esté pagada
        if (!ordenActual.getEstado().equalsIgnoreCase("Pagada")){
            JOptionPane.showMessageDialog(null,"Primero debe marcar la orden como pagada.");
            return;
        }

        ArchivosOrdenes archivo = new ArchivosOrdenes();
        ArrayList<Orden> ordenes = archivo.leerOrdenes();
        boolean ordenExistente = false;

        for (Orden orden : ordenes){
            if (orden.getId() == ordenActual.getId()){
                ordenExistente = true;
                // Actualiza la orden existente
                orden.setEstadoCocina("Pendiente");
                break;
            }
        }

        if (!ordenExistente){
            ordenActual.setEstadoCocina("Pendiente");
            ordenes.add(ordenActual);
        }

        ArchivosOrdenes.guardarOrdenes(ordenes);

        JOptionPane.showMessageDialog(null,"Orden enviada a cocina correctamente.");
        // Limpia la orden actual
        ordenActual = null;
        txtOrdenActual.setText("No hay una orden creada.");
    }//GEN-LAST:event_BEnviarCocinaActionPerformed

