    private void BHistoricoOrdenesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BHistoricoOrdenesActionPerformed
        ArchivosOrdenes archivo = new ArchivosOrdenes();

        ArrayList<Orden> ordenes = archivo.leerOrdenes();

        String historico = "HISTÓRICO DE ÓRDENES\n";
        historico += "====================================\n\n";

        boolean hayOrdenesPagadas = false;

        for (Orden orden : ordenes){
            //Muestra las órdenes pagadas
            if (orden.getEstado().equalsIgnoreCase("Pagada")){
                hayOrdenesPagadas = true;
                
                historico += "Orden #: "+ orden.getId()+ "\n";
                historico += "Cliente: "+ orden.getCliente() + "\n";
                historico += "Estado de pago: "+ orden.getEstado()+ "\n";
                historico += "Estado de cocina: "+ orden.getEstadoCocina()+ "\n";
                historico += "Detalle:\n";

                for (ItemOrden item : orden.getItems()){
                    if (item.esProducto()){
                        historico += item.getCantidad()+ " x "+ item.getProducto().getNombre()+ " - ₡"+ String.format("%.2f",item.getSubtotal())+ "\n";

                    } 
                    else if (item.esCombo()){
                        historico += item.getCantidad()+ " x Combo: "+ item.getCombo().getNombre()+ " - ₡"+ String.format("%.2f",item.getSubtotal())+ "\n";
                    }
                }

                double total = orden.calcularTotal();
                double subtotal = total / 1.13;
                double iva = total - subtotal;

                historico += "\nSubtotal sin IVA: ₡"+ String.format("%.2f", subtotal)+ "\n";
                historico += "IVA incluido: ₡"+ String.format("%.2f", iva)+ "\n";
                historico += "Total de la orden: ₡" + String.format("%.2f", total)+ "\n";
                historico += "------------------------------------\n\n";
            }
        }

        if (!hayOrdenesPagadas){
            JOptionPane.showMessageDialog(null,"No hay órdenes pagadas en el histórico.");

            return;
        }

        JOptionPane.showMessageDialog(null,historico,"Histórico de Órdenes",JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_BHistoricoOrdenesActionPerformed

