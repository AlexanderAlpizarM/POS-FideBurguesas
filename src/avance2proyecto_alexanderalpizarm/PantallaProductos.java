    private void btnMostrarProductosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarProductosActionPerformed
        ArchivosProductos archivo = new ArchivosProductos();
        ArrayList<Producto> productos = archivo.leerProductos();

        txtMostrarProductos.setText("");
        txtMostrarProductos.append("ID\tNombre\t\tPrecio\tCategoría\n");
        txtMostrarProductos.append("------------------------------------------------------------\n");

        if (productos.isEmpty()) {
            txtMostrarProductos.append("No hay productos registrados.\n");

            return;
        }

        for (Producto producto : productos){
            txtMostrarProductos.append(producto.getId() + "\t"+ producto.getNombre() + "\t\t₡"+ producto.getPrecio() + "\t"
                    + producto.getCategoria() + "\n");
        }
    }//GEN-LAST:event_btnMostrarProductosActionPerformed

