    private void btnRegistrarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarProductoActionPerformed
        String datoId = JOptionPane.showInputDialog("Ingrese el ID del producto:");

        if (datoId == null || datoId.trim().isEmpty()){
            JOptionPane.showMessageDialog(null,"Debe ingresar el ID del producto.");

            return;
        }

        String nombre = JOptionPane.showInputDialog("Ingrese el nombre del producto:");

        if (nombre == null || nombre.trim().isEmpty()){
            JOptionPane.showMessageDialog(null,"Debe ingresar el nombre del producto.");

            return;
        }

        String datoPrecio = JOptionPane.showInputDialog("Ingrese el precio del producto:");

        if (datoPrecio == null || datoPrecio.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe ingresar el precio del producto.");

            return;
        }

        String categoria = JOptionPane.showInputDialog("Ingrese la categoría del producto:");

        if (categoria == null || categoria.trim().isEmpty()){
            JOptionPane.showMessageDialog(null,"Debe ingresar la categoría del producto.");

            return;
        }

        try {

            int id = Integer.parseInt(datoId);
            double precio = Double.parseDouble(datoPrecio);

            if (id <= 0){

                JOptionPane.showMessageDialog(null,"El ID debe ser mayor a cero.");

                return;
            }

            if (precio <= 0){
                JOptionPane.showMessageDialog(null, "El precio debe ser mayor a cero.");

                return;
            }

            ArchivosProductos archivo = new ArchivosProductos();
            ArrayList<Producto> productos = archivo.leerProductos();

            //Valida que el ID no esté repetido
            for (Producto producto : productos){

                if (producto.getId() == id){
                    JOptionPane.showMessageDialog(null,"Ya existe un producto con ese ID.");

                    return;
                }
            }

            Producto nuevoProducto = new Producto(id,nombre,precio,categoria);
            productos.add(nuevoProducto);
            ArchivosProductos.guardarProductos(productos);
            nuevoProducto.registrarProducto();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "El ID y el precio deben ser números válidos.");
        }
    }//GEN-LAST:event_btnRegistrarProductoActionPerformed

    private void btnModificarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarProductoActionPerformed
        ArchivosProductos archivo = new ArchivosProductos();

        ArrayList<Producto> productos = archivo.leerProductos();

        String datoId = JOptionPane.showInputDialog("Ingrese el ID del producto que desea modificar:");

        if (datoId == null || datoId.trim().isEmpty()){
            return;
        }

        try {

            int id = Integer.parseInt(datoId);

            boolean encontrado = false;

            for (Producto producto : productos){
                if (producto.getId() == id){

                    String nuevoNombre = JOptionPane.showInputDialog("Ingrese el nuevo nombre:",producto.getNombre());

                    if (nuevoNombre == null || nuevoNombre.trim().isEmpty()){
                        JOptionPane.showMessageDialog(null, "Debe ingresar un nombre.");

                        return;
                    }

                    String datoPrecio = JOptionPane.showInputDialog("Ingrese el nuevo precio:",producto.getPrecio());

                    if (datoPrecio == null || datoPrecio.trim().isEmpty()) {
                        JOptionPane.showMessageDialog(null,"Debe ingresar un precio.");

                        return;
                    }

                    String nuevaCategoria = JOptionPane.showInputDialog("Ingrese la nueva categoría:",producto.getCategoria());

                    if (nuevaCategoria == null || nuevaCategoria.trim().isEmpty()){
                        JOptionPane.showMessageDialog(null, "Debe ingresar una categoría.");

                        return;
                    }

                    double nuevoPrecio = Double.parseDouble(datoPrecio);

                    if (nuevoPrecio <= 0){
                        JOptionPane.showMessageDialog(null,"El precio debe ser mayor a cero.");

                        return;
                    }

                    producto.setNombre(nuevoNombre);
                    producto.setPrecio(nuevoPrecio);
                    producto.setCategoria(nuevaCategoria);

                    ArchivosProductos.guardarProductos(productos);
                    producto.modificarProducto();

                    encontrado = true;

                    break;
                }
            }

            if (!encontrado){
                JOptionPane.showMessageDialog(null, "No existe un producto con ese ID.");
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null,"El ID y el precio deben ser números válidos.");
        }
    }//GEN-LAST:event_btnModificarProductoActionPerformed

    private void btnEliminarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarProductoActionPerformed
        ArchivosProductos archivo = new ArchivosProductos();

        ArrayList<Producto> productos = archivo.leerProductos();

        String datoId = JOptionPane.showInputDialog("Ingrese el ID del producto que desea eliminar:");

        if (datoId == null || datoId.trim().isEmpty()){
            return;
        }

        try {

            int id = Integer.parseInt(datoId);

            boolean encontrado = false;

            for (Producto producto : productos){
                if (producto.getId() == id){
                    int opcion = JOptionPane.showConfirmDialog(null,"¿Está seguro de eliminar el producto?","Eliminar Producto",JOptionPane.YES_NO_OPTION);
                    if (opcion == JOptionPane.YES_OPTION){
                        productos.remove(producto);
                        ArchivosProductos.guardarProductos(productos);
                        producto.eliminarProducto();
                    }
                    encontrado = true;
                    break;
                }
            }

            if (!encontrado){
                JOptionPane.showMessageDialog(null,"No existe un producto con ese ID.");
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null,"Debe ingresar un ID válido.");

        }
    }//GEN-LAST:event_btnEliminarProductoActionPerformed

